package utill.collection;

public class WordRank {

	/* ---- Data Members ---- */
	
	private String word;
	
	private int counter;

	public String getWord() {
		return word;
	}

	public void setWord(String word) {
		this.word = word;
	}

	public int getCounter() {
		return counter;
	}

	public void setCounter(int counter) {
		this.counter = counter;
	}
	
	
}
